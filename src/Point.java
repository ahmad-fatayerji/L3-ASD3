
public class Point{

    private int x; // Coordonnée X
    private int y; // Coordonnée Y

    // Constructeur
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}